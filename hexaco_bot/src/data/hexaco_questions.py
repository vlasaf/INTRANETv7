def get_question(question_number: int) -> Tuple[str, str, bool]:
    """Get question by number (1-100)."""
    if 1 <= question_number <= 100:
        return HEXACO_QUESTIONS[question_number - 1]
    else:
        raise ValueError(f"Question number must be between 1 and 100, got {question_number}")

def get_questions_by_factor(factor: str) -> List[Tuple[int, str, bool]]:
    """Get all questions for a specific factor."""
    questions = []
    for i, (text, q_factor, reverse) in enumerate(HEXACO_QUESTIONS, 1):
        if q_factor == factor:
            questions.append((i, text, reverse))
    return questions

def get_total_questions() -> int:
    """Get total number of questions."""
    return len(HEXACO_QUESTIONS)

def get_factor_name(factor_code: str) -> str:
    """Get full factor name from code."""
    return FACTORS.get(factor_code, "Unknown Factor") 